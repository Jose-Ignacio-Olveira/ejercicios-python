from Idioma import Idioma
class Ingles(Idioma):
    def saludar(self) -> str:
        return "Hello"
    
    def despedirse(self) -> str:
        return "Goodbye"
    
    def perdon(self) -> str:
        return "Sorry"
    
    def pedir_cafe(self) -> str:
        return "Can I have a coffee?"
    
    def cuanto_cuesta(self) -> str:
        return "How much does it cost?"
    
    def donde_queda(self) -> str:
        return "Where is it?"
